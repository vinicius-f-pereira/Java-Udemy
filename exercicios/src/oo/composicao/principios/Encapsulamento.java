public class Encapsulamento {

//	Colocar algo dentro de uma capsula. 
//	public, visivel pra quem vai usar. 
//	privado, "escondido" de quem está usando. 
//	se tudo que você criar estiver visivel pode tornar o relacionamento entre objetos mais difícil.
//	a capsula é um objeto, ele agrupa atributos e comportamentos.
	
// PRIVATE - Quando você cria um atributo privado ele só pode ser acessado dentro da própria classe. S
// somente os metodos criados dentro da classe podem acessa-lo. 

//	PUBLIC - Qualquer classe do sistema podem acessar o membro público.
	
//	PROTECTED - da para acessar um atributo em outro pacote desde que ele seja herdado. Mais sobre em outra aula.
	
//	PACKAGE - Ele só é acessivel em classes que estão dentro do mesmo pacote(package).
	
//	PACKAGE é o único que não necessita de palavra reservada. Quando não coloca o nível de visibilidade, ele toma coma padrão o PACKAGE.
	
	
}
